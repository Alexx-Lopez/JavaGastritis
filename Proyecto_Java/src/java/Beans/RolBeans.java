public class RolBeans {

    private String txt_rol;
    private String cmb_rol;
    private String txt_descripcion;
    private String txt_prestamos;
    
    /**
     * @return the txt_rol
     */
    public String getTxt_rol() {
        return txt_rol;
    }

    /**
     * @param txt_rol the txt_rol to set
     */
    public void setTxt_rol(String txt_rol) {
        this.txt_rol = txt_rol;
    }

    /**
     * @return the cmb_rol
     */
    public String getCmb_rol() {
        return cmb_rol;
    }

    /**
     * @param cmb_rol the cmb_rol to set
     */
    public void setCmb_rol(String cmb_rol) {
        this.cmb_rol = cmb_rol;
    }

    /**
     * @return the txt_descripcion
     */
    public String getTxt_descripcion() {
        return txt_descripcion;
    }

    /**
     * @param txt_descripcion the txt_descripcion to set
     */
    public void setTxt_descripcion(String txt_descripcion) {
        this.txt_descripcion = txt_descripcion;
    }

    /**
     * @return the txt_prestamos
     */
    public String getTxt_prestamos() {
        return txt_prestamos;
    }

    /**
     * @param txt_prestamos the txt_prestamos to set
     */
    public void setTxt_prestamos(String txt_prestamos) {
        this.txt_prestamos = txt_prestamos;
    }
}

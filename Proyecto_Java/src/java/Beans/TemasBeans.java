public class TemasBeans {

    private String cmb_temas;
    private String txt_temas; 
    private String txt_cod_mat;
        
    /**
     * @return the cmb_temas
     */
    public String getCmb_temas() {
        return cmb_temas;
    }

    /**
     * @param cmb_temas the cmb_temas to set
     */
    public void setCmb_temas(String cmb_temas) {
        this.cmb_temas = cmb_temas;
    }

    /**
     * @return the txt_temas
     */
    public String getTxt_temas() {
        return txt_temas;
    }

    /**
     * @param txt_temas the txt_temas to set
     */
    public void setTxt_temas(String txt_temas) {
        this.txt_temas = txt_temas;
    }

    /**
     * @return the txt_cod_mat
     */
    public String getTxt_cod_mat() {
        return txt_cod_mat;
    }

    /**
     * @param txt_cod_mat the txt_cod_mat to set
     */
    public void setTxt_cod_mat(String txt_cod_mat) {
        this.txt_cod_mat = txt_cod_mat;
    }
    
}
